package scaler.com;

public class TimeUnitConstants {

    public static final int DAYS_OF_A_YEAR = 365;

    public static final int SECONDS_OF_A_MINUTE = 60;
    public static final int SECONDS_OF_AN_HOUR = 60 * SECONDS_OF_A_MINUTE;
    public static final int SECONDS_OF_A_DAY = 24 * SECONDS_OF_AN_HOUR;
    public static final int SECONDS_OF_A_YEAR = DAYS_OF_A_YEAR * SECONDS_OF_A_DAY;
}
